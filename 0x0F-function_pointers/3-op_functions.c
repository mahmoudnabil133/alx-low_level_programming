#include "3-calc.h"
/**
 * op_add - adds 2nums
 * @a: is a
 * @b: is b
 * Return: sum of 2 nums
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub 2 nums
 * @a: is a
 * @b: is b
 * Return: sub 2 nums.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul 2 nums
 * @a: is a
 * @b: is b
 * Return: the ret mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - dev 2 nums
 * @a: is a
 * @b: is b
 * Return: div 2 nums
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod 3 nums
 * @a: is a
 * @b: is b
 * Return: mod of 2
 */
int op_mod(int a, int b)
{
	return (a % b);
}
